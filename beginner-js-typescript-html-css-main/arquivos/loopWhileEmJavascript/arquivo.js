var contagem=0;
var percorrido=0;
while(contagem<=4){
    percorrido=percorrido+50;
    contagem=contagem+1;
    window.alert("o total percorrido foi "+percorrido+" a contagem total foi "+contagem);       
}
while(contagem>0){
    percorrido=percorrido+50;
    contagem=contagem-1;
    window.alert("o total percorrido foi "+percorrido+" a contagem total foi "+contagem);       
}